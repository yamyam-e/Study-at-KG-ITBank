
public class J06_for {
	
//	for(초기식; 조건식; 증감식) {
//		반복명령
//  }
	public static void main(String[] args) {
		
//		1. 1~10 범위의 정수 출력
		for(int i=1; i<=10; i++) {
			System.out.println("i = " + i);
		}
//		2. "안녕"을 4번 출력
		for(int i=1; i<=4; i++) {
			System.out.println("안녕");
		}
		
	}

}
