
public class J07_intro {
	
	public static void main(String[] args) {
		
		//1년은 몇초?
		//1년 -> 365일 -> 24시간 -> 60분 -> 60초
		System.out.println("1년은 " + (365*24*60*60) +"초다.");
		System.out.println("2년은 " + (365*24*60*60*2) +"초다.");
		System.out.println("3년은 " + (365*24*60*60*3) +"초다.");
		System.out.println("4년은 " + (365*24*60*60*4) +"초다.");
		System.out.println("5년은 " + (365*24*60*60*5) +"초다.");
		
		//=>변수를 사용하면 효율적인 프로그래밍 가능
		
	}

}
